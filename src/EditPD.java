    private void editPDMNOKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_editPDMNOKeyTyped
        // SETTING TEXTFIELD TO TAKE ONLY NUMERICAL VALES
        //ALSO TO MAKE IT BEEP WHEN ALPHABETS ARE CLICKED
        char c =  evt.getKeyChar();
        //code to allow backspace and delete condition
        if (! (Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || c == KeyEvent.VK_DELETE)){
            getToolkit().beep();

            evt.consume();
        }
    }//GEN-LAST:event_editPDMNOKeyTyped

