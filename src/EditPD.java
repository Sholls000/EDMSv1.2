    private void editPDONKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_editPDONKeyTyped
        // SETTING TEXTFIELD TO TAKE ONLY NUMERICAL VALES
        //ALSO TO MAKE IT BEEP WHEN ALPHABETS ARE CLICKED
        char cc =  evt.getKeyChar();
        //code to allow backspace and delete condition
        if (! (Character.isDigit(cc) || (cc == KeyEvent.VK_BACK_SPACE ) || cc == KeyEvent.VK_DELETE)){
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_editPDONKeyTyped

