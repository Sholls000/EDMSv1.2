        //code to allow backspace and delete condition
        if (! (Character.isDigit(c6) || (c6 == KeyEvent.VK_BACKSPACE ) || c6 == KeyEvent.VK_DELETE)){
            getToolkit().beep();

            evt.consume();
        }
