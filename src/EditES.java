    private void editjButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editjButton8ActionPerformed
        //get default webcam and open
        webcam.open();
        // webcam.setViewSize(WebcamResolution.VGA.getSize());

        //setting webpanel display properties
        panel.setFPSDisplayed(true);
        panel.setDisplayDebugInfo(true);
        panel.setImageSizeDisplayed(true);
        panel.setMirrored(true);
        panel.setFillArea(true);

        BufferedImage image1 = webcam.getImage();

        //get image
        try {
            ImageIO.write(image1, "PNG", new File("test.png"));

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "This one na gobe");

        }

        //setting buffered image to imageicon
        immaaged2 = new ImageIcon(image1);

        //setting imageicon to Jlabel
        editJESEP.setIcon(immaaged2);
    }//GEN-LAST:event_editjButton8ActionPerformed

